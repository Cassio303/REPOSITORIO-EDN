A = int(input("Digite o valor de A : "))
B = int(input("Digite o valor de B : "))
C = int(input("Digite o valor de C : "))
D = int(input("Digite o valor de D : "))

DIFERENCA = (A * B) - (C * D)

print(f"O valor de A é {A}")
print(f"O valor de B é {B}")
print(f"O valor de C é {C}")
print(f"O valor de D é {D}")
print("A formula para a diferença é: (A * B) - (C * D)")
print(f"Substituindo os valores fornecidos: DIFERENÇA = ({A} * {B}) - ({C} * {D})")
print(f"O resultado da diferença é: {DIFERENCA}")
